export function installElementPlus(app: App) {
  components.forEach(component => {
    if (component.name) {
      app.component(component.name, component)
    }
  })

  Object.entries(icons).forEach(([name, component]) => {
    app.component(name, component)
  })

  app.use(ElLoading)
}
